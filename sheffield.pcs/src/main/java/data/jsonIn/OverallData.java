package data.jsonin;

public class OverallData {
    String name;
    int count;

    PC_info results;

    public String getName() {
	return name;
    }

    public void setName(String name) {
	this.name = name;
    }

    public int getCount() {
	return count;
    }

    public void setCount(int count) {
	this.count = count;
    }

    public PC_info getResults() {
	return results;
    }

    public void setResults(PC_info results) {
	this.results = results;
    }
}
